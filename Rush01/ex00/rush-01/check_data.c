#include <unistd.h>

int	check_data(char *val)
{
	int i;
	int j;
	int k;

     i = 0;
	 j = 0;
	 k = 0;

	while (val[i])
	{
		if (i % 2 == 0)
		{
			if (val[i] >= '1' && val[i] <= '4')
				j++;
		}
		else	
		{
			if (val[i] == ' ')
				k++;
		}
		i++;
	}
	if (j == 16 && k == 15 && i == 31)
		return (1);
	write(1,"Error", 5);
	return (0);
}
